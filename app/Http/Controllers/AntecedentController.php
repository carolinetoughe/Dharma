     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'nom' => 'required',
            'user_id' => 'required',
    
        ]);
        $form_data = array(
            'nom'       =>   $request->nom,
            'user_id'        =>   $request->user_id,
        );
        Antecedent::create($form_data);
        return redirect()->route('patientliste')
